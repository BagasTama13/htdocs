                    <h1 class="mb-4">Chronic kidney disease</h1>
                    <p >Penyakit Gagal Ginjal Kronik / Chronic Kidney Disease (CKD) merupakan suatu keadaan saat ginjal mengalami percepatan kehilangan fungsi ekskresi, hormonal, dan metabolik yang sifatnya tidak bisa dikembalikan. Fungsi ekskesi ginjal adalah melakukan pengeluaran produk akhir metabolisme yang tidak diperlukan oleh tubuh, seperti urea. Fungsi endokrin ginjal adalah memproduksi enzim dan hormon, seperti renin untuk pengaturan tekanan darah, eritopoitein untuk sintesis eritrosit, dan mengatur metabolisme kalsium (Ramayulis, 2016). Penurunan fungsi ginjal pada CKD berlangsung perlahan, mulai dari CKD tahap 1 sampai dengan tahap 5.                    </p>
                    <p style="color: red; "><i><b>Menurut Almatsier (2008),</b></i></p><p>pada CKD tahap 5, pasien dianjurkan untuk menerima terapi dialisis. Dialisis dilakukan dimana ginjal tidak mampu lagi mengeluarkan produk-produk sisa metabolisme, mempertahankan keseimbangan cairan dan elektrolit, serta memproduksi hormon-hormon. Dialisis dilakukan apabila hasil Glomerular Filtration Rate (GFR) < 15ml/menit. Dialisis yang paling banyak dilakukan adalah hemodialisis.</p>
                    <h4 class="mb-4">Apa itu hemodialisis</h4>
                    <p>Hemodialisa didefiniskan sebagai pergerakan larutan dan air dari darah pasien melewati membran semipermiabel (dializer) ke dalam dialisat (Nuari dan Dhina, 2017). Pada proses hemodialisis, aliran darah ke ginjal dialihkan melalui membran semipermiabel dari mesin cuci ginjal sehingga produk-produk sisa metabolisme dapat dikeluarkan dari tubuh. Menurut Sitasari, dkk (2017), hemodialisis menggantikan fungsi ekskresi dari ginjal. Fungsi metabolik dan hormonal dapat digantikan dengan terapi farmakologik dan terapi gizi.<br><br>Pada saat hemodialisis, terjadi kehilangan darah, vitamin larut air. Kehilangan 4-9 gram asam amino, dan 2-3 gram peptida terbuang setiap satu sesi hemodialisis yaitu sekitar 4-5 jam. Hemodialisis dapat menyebabkan terjadinya suatu peradangan, menyebabkan pemecahan protein tubuh, sehingga hal tersebut dapat mengakibatkan penurunan berat badan dan status gizi.</p>
                    <h4 class="mb4">Pengaturan Makan / Diet pada Pasien Hemodialisis</h4>
                    <p>Pada pasien dengan hemodialisis, diharpakan mengkonsumsi makanan yang cukup energi dan mengkonsumsi makanan tinggi protein untuk menggantikan kehilangan asam amino dan zat gizi lain yang hilang selama proses hemodialisis. Anjuran diet didasarkan pada frekuensi dialisis, sisa fungsi ginjal, dan ukuran tubuh.</p>
                    <h5 class="mb-4">Tujuan diet yang diberikan untuk pasien dengan hemodialisis menurut <i style="color: red;">Cornelia (2013)</i>, yaitu:</h5>
                    <ol type="1" style="color: black;">
                        <li>Mengurangi penumpukan sampah uremi</li>
                        <li>Mengurangi penumpukan cairan dan elektrolit di luar waktu dialisis</li>
                        <li>Memperbaiki status gizi</li>
                        <li>Mencegah defisiensi protein, asam amino, dan vitamin</li>
                    </ol>
                    <h4 class="mb-4">Makanan yang dianjurkan:</h4>
                    <ol type="1">
                        <li>Makanan sumber energi, seperti: nasi, roti, mie, makaroni, spageti, lontong, bihun, madu, permen. Makanan sumber energi berguna menjaga atau memperbaiki status gizi pasien.</li><br>
                        <li>Makanan sumber protein, seperti: telur, ayam, daging, ikan, kacang-kacangan termasuk tahu dan tempe dalam jumlah yang terbatas disesuaikan dengan perhitungan kebutuhan gizi. Pada pasien dengan hemodialisis, protein berfungsi untuk mejaga kekuatan otot dan daya tahan tubuh pasien.</li>
                    </ol>
