                    <h1 class="mb-4">Chronic kidney disease</h1>
                    <p >Gagal ginjal kronis adalah ketika fungsi ginjal menurun secara bertahap akibat kerusakan jaringan ginjal. Secara medis, gagal ginjal kronis didefinisikan sebagai penurunan laju penyaringan ginjal selama 3 bulan atau lebih. Fungsi utama ginjal adalah menyaring limbah atau zat sisa metabolisme tubuh dan kelebihan cairan dari darah untuk dibuang melalui urine. Setiap hari, kedua ginjal menyaring sekitar 120–150 liter darah dan menghasilkan sekitar 1–2 liter urine. </p>
                    <p class="mb-4">Di dalam ginjal, terdapat unit penyaring bernama nefron yang terdiri dari glomerulus dan tubulus. Glomerulus menyaring cairan dan limbah untuk dikeluarkan, tetapi mencegah sel darah dan protein darah keluar dari tubuh. Selanjutnya, mineral yang dibutuhkan tubuh akan diserap di tubulus agar tidak terbuang bersama urine.</p>
                    <h5 class="mb-4">Selain menyaring limbah dan kelebihan cairan, ginjal juga berfungsi untuk:</h5>
                    <p><i class="far fa-circle text-primary me-3"></i>Menghasilkan enzim renin yang menjaga tekanan darah dan kadar garam dalam tubuh tetap normal</p>
                    <p><i class="far fa-circle text-primary me-3"></i>Membuat hormon eritropoietin yang merangsang sumsum tulang untuk memproduksi sel darah merah</p>
                    <p><i class="far fa-circle text-primary me-3"></i>Memproduksi vitamin Ddalam bentuk aktif yang bermanfaat untuk menjaga kesehatan tulang</p>
                    <p>Gagal ginjal kronis atau penyakit ginjal kronis menyebabkan cairan, elektrolit, dan limbah menumpuk di dalam tubuh dan menimbulkan gangguan. Gejala bisa lebih terasa ketika fungsi ginjal memburuk. Pada tahap lanjut, gagal ginjal kronis dapat berakibat fatal, terutama jika tidak ditangani, misalnya dengan cuci darah.</p>
                    <p>Data penelitian menunjukkan bahwa kebanyakan gagal ginjal kronis di Indonesia terjadi akibat hipertensi dan diabetes (nefropati diabetik) yang tidak terkontrol.</p>
                    <h4 class="mb-4">Penyebab dan Gejala Gagal Ginjal Kronis</h4>
                    <p>Gagal ginjal kronis disebabkan oleh kerusakan jaringan ginjal yang dipicu oleh penyakit jangka panjang. Beberapa penyakit yang bisa menjadi penyebab gagal ginjal adalah diabetes, tekanan darah tinggi, dan penyakit asam urat.</p>
                    <p>Gejala pada penderita gagal ginjal kronis stadium 1–3 biasanya tidak begitu terlihat. Umumnya, gejala gagal ginjal kronis baru terasa ketika sudah mencapai stadium 4 dan 5. Pada kondisi ini, gangguan metabolisme tubuh sudah berat karena ginjal tidak dapat menyaring racun.</p>
                    <h6 class="mb-4">Gejala yang ditemukan pada penderita gagal ginjal kronis antara lain:</h6>
                    <p><i class="far fa-circle text-primary me-3"></i>Tekanan darah tinggi yang tidak terkendali</p>
                    <p><i class="far fa-circle text-primary me-3"></i>Bengkak pada kaki dan pergelangan kaki</p>
                    <p><i class="far fa-circle text-primary me-3"></i>Buang air kecil menjadi sedikit (oligouria)</p>
                    <p><i class="far fa-circle text-primary me-3"></i>Pucat dan lemas</p>

                    <h3 class="mb-4">Diagnosis Gagal Ginjal Kronis</h3>
                    <p>Diagnosis gagal ginjal kronis dilakukan dengan menanyakan gejala, serta riwayat penyakit pasien dan keluarganya, diikuti dengan tes fisik. Dokter juga akan melakukan tes penunjang untuk menilai fungsi ginjal dan mendeteksi kerusakan ginjal.</p>
                    <h4 class="mb-4">pemeriksaan tersebut meliputi:</h4>

                    <h5 class="mb-4">Test Darah</h5>
                    <p>Tes darah dilakukan untuk mengetahui kerja ginjal dengan memeriksa kadar limbah dalam darah, seperti kreatinin dan ureum.</p>
                    <h5 class="mb-4">Test Urine</h5>
                    <p>Dalam tes ini, kadar protein darah (albumin), kreatinin, dan sel darah merah dalam urine akan diperiksa. Hasil pemeriksaan tersebut bisa menunjukkan seberapa parah kerusakan ginjal yang dialami pasien.</p>
                    <h5 class="mb-4">Pemindaian</h5>
                    <p>Pemindaian ini bertujuan melihat struktur dan ukuran ginjal. Umumnya, pemeriksaan yang dilakukan adalah USG ginjal, tetapi bisa juga menggunakan MRI atau CT scan.</p>
                    <h5 class="mb-4">Biopsi Ginjal</h5>
                    <p>Biopsi ginjal dilakukan dengan mengambil sedikit sampel dari jaringan ginjal. Sampel ini selanjutnya akan dianalisis di laboratorium agar penyebab kerusakan ginjal dapat diketahui.<br>Melalui hasil pemeriksaan di atas, dokter akan menghitung perkiraan laju filtrasi glomerulus (LFG). Perhitungan ini dapat menentukan stadium gagal ginjal kronis pasien dan metode pengobatan yang tepat.</p>
                    <h6 class="mb-4">Berdasarkan pemeriksaan LFG, stadium gagal ginjal dapat dikategorikan menjadi:</h6>
                    <ol type="1">
                        <li>Stadium 1, nilai LFG di atas 90 mL/menit/1,73 m2</li>
                        <li>Stadium 2, nilai LFG 60 hingga 89 mL/menit/1,73 m2</li>
                        <li>Stadium 3, nilai LFG 30 hingga 59 mL/menit/1,73 m2</li>
                        <li>Stadium 4, nilai LFG 15 hingga 29 mL/menit/1,73 m2</li>
                        <li>Stadium 5, nilai LFG di bawah 15 mL/menit/1,73 m2</li>
                    </ol>
                    <p>Perlu diketahui, nilai kisaran di atas tidak dapat digunakan untuk menentukan stadium gagal ginjal kronis. Hal ini karena orang yang berusia lanjut bisa memiliki nilai LFG yang setara dengan gagal ginjal kronis stadium 2 meski ia tidak menderita penyakit ginjal.</p>
